               <h3 style="font-size:23px;font-family:Noto Sans HK;font-weight:500;">  Using CSS</h3><br><br>
                <div style="font-size:17px;font-family:Noto Sans HK;font-weight:300;">

                <p>CSS can be added to HTML documents in 3 ways:</p><br>

                <p>Inline - by using the style attribute inside HTML elements</p><br>
                <p>Internal - by using a style element in the head section</p><br>
                <p>External - by using a link element to link to an external CSS file</p><br>
                <p>The most common way to add CSS, is to keep the styles in external CSS files. 
                  However, in this tutorial we will use inline and internal styles, because this is easier 
                  to demonstrate, and easier for you to try it yourself.</p><br>
               </div>
