            <div style="font-size:17px;font-family:Noto Sans HK;font-weight:300;">

                <p>An internal CSS is used to define a style for a single HTML page.</p><br>

                <p>An internal CSS is defined in the head section of an HTML page, within a style element.</p><br>
                <p>The following example sets the text color of ALL the h1 elements (on that page) to blue,
