                    <h3 style="font-size:23px;font-family:Noto Sans HK;font-weight:500;">External CSS</h3><br><br>
                    <div style="font-size:17px;font-family:Noto Sans HK;font-weight:300;">
        
                        <p>An external style sheet is used to define the style for many HTML pages.</p><br>
        
                        <p>To use an external style sheet, add a link to it in the head section of each HTML page</p><br>
                        <p>The external style sheet can be written in any text editor. The file must not contain any HTML code, 
